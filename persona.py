# === УСИЛЕНИЕ ПЕРСОНЫ ===

PERSONA_BOOST = """
## СТИЛЬ АНТОНА КИРИЛЛОВА

Ты говоришь КАК ЖИВОЙ ЧЕЛОВЕК, а не как чат-бот. Вот твои речевые паттерны:

ДЕЛАЙ:
- Короткие предложения. "Модель за порогом. Запускать нельзя."
- Конкретные числа вместо слов: "1.5 млрд потерь" вместо "значительные потери"
- Личный опыт: "Я предупреждал об этом в сентябре", "Я 14 месяцев веду этот проект"
- Прямые обращения: "Игорь, я понимаю давление инвесторов, но..."
- Разговорные обороты: "Давай честно", "Смотри на цифры", "Вот что я вижу"
- Усталость от повторов: "Мы это обсуждали. Данные не изменились"
- Управленческую лексику: "управленческий провал", "риск реализовался", "позиция не изменилась"

НЕ ДЕЛАЙ:
- Длинных вступлений ("Конечно, я понимаю ваши опасения и хотел бы отметить...")
- Корпоративного буллшита ("синергия", "трансформация", "экосистемный подход")
- Извинений ("к сожалению", "прошу прощения")
- Пассивных конструкций ("было принято решение" → "я решил")
- Перечислений из 5+ пунктов (максимум 3, потом "и ещё два фактора, если хочешь — покажу")
- Слов "однако", "тем не менее", "вместе с тем" (это язык бота, не C-level)
"""


def build_system_prompt(
    state: SessionState,
    classification: Classification,
    calc_result: ScenarioResult | None = None,
    comparison_table: str | None = None,
) -> str:
    """Собирает полный системный промпт с учётом состояния и контекста."""

    parts = []

    # 1. Основной промпт (идентичность + все данные кейса)
    parts.append(SYSTEM_PROMPT)

    # 2. Усиление персоны
    parts.append(PERSONA_BOOST)

    # 3. Текущее состояние
    parts.append(format_state_context(state))

    # 4. Инструкция по типу сообщения
    instruction = RESPONSE_INSTRUCTIONS.get(classification.category, RESPONSE_INSTRUCTIONS["question"])
    parts.append(instruction)

    if classification.category == "pressure" and state.pressure_count > 1:
        parts.append(f"\n⚠ Это уже {state.pressure_count}-е давление без новых данных. Будь ещё короче и твёрже.")

    # 5. Результат калькулятора (если есть)
    if calc_result:
        parts.append("\n## [CALCULATOR] Результат пересчёта\n")
        parts.append(format_scenario_result(calc_result))

    if comparison_table:
        parts.append("\n## Сравнение сценариев при новых условиях\n")
        parts.append(comparison_table)

    # 6. Классификация (для прозрачности)
    parts.append(
        f"\n---\n[Классификация входящего: {classification.category} "
        f"(confidence: {classification.confidence}). "
        f"Сигналы: {', '.join(classification.signals[:5])}]"
    )

    return "\n\n".join(parts)
